package com;

public class Operaciones {

    public double Suma(int x, int y) {
        //Metodo para sumar
	double resultado;
        resultado = x + y;
        return resultado;
    }
    
    public double Resta(int x, int y) {
        //Metodo para restar
	double resultado;
        resultado = x - y;
        return resultado;
    }
    
    public double Multiplicacion(int x, int y) {
        double resultado;
        resultado = x * y;
        return resultado;
    }
    
    public double Division(int x, int y) {
        double resultado;
        resultado = x / y;
        return resultado;
    }
    
}
